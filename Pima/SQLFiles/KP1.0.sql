--DROP TABLE "User"
CREATE TABLE "User"
(
    Id NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    Login NVARCHAR2(50) NOT NULL,
    Password NVARCHAR2(50) NOT NULL,
    Foto BLOB,
    CONSTRAINT PK_USER PRIMARY KEY (Id)
);

--DROP TABLE "Article"
CREATE TABLE "Article" 
(
    Id NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    Title NVARCHAR2(500),
    Text NVARCHAR2(2000),
    Image BLOB,
    CONSTRAINT PK_ARTICLE PRIMARY KEY (Id)
);

--DROP TABLE "Notes"
CREATE TABLE "Notes"
(
    Id NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    Name NVARCHAR2(50),
    Author NVARCHAR2(50),
    Note BLOB,
    Description NVARCHAR2(100),
    CONSTRAINT PK_NOTES PRIMARY KEY (Id)
);

--DROP TABLE "Chords"
CREATE TABLE "Chords"
(
    Id NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    Name NVARCHAR2(50),
    Chord BLOB,
    CONSTRAINT PK_CHORDS PRIMARY KEY (Id)
);

--DROP TABLE "TABs"
CREATE TABLE "TABs"
(
    Id NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    Name NVARCHAR2(50),
    Author NVARCHAR2(50),
    TAB BLOB,
    Description NVARCHAR2(100),
    CONSTRAINT PK_TABS PRIMARY KEY (Id)
);

--DROP TABLE "Songs"
CREATE TABLE "Songs"
(
    Id NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    Name NVARCHAR2(50),
    Author NVARCHAR2(50),
    Text NVARCHAR2(1000),
    Music BLOB,
    Description NVARCHAR2(100),
    CONSTRAINT PK_SONGS PRIMARY KEY (Id)
);

--DROP TABLE "ChordSong"
CREATE TABLE "ChordSong"
(
    Id NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    IdSong NUMBER,
    IdChord NUMBER,
    CONSTRAINT PK_CHORDSONG PRIMARY KEY (Id),
    CONSTRAINT FK_CHORD FOREIGN KEY (IdChord) REFERENCES Chords(Id),
    CONSTRAINT FK_SONG FOREIGN KEY (IdSong) REFERENCES Songs(Id)
);

